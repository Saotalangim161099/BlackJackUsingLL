public class Move {
    String phoneNumber;
    String move;

    public Move(String phoneNumber, String move) {
        this.phoneNumber = phoneNumber;
        this.move = move;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getMove() {
        return move;
    }

    public void setMove(String move) {
        this.move = move;
    }
}
